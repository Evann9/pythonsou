# [일원 카이제곱 검정 (One-way Chi-square test)]
# 목적: 적합도 검정 (Goodness of Fit)
# 내용: 관찰된 빈도가 특정 이론적 분포(기대 빈도)를 따르는지 검정
# 대상: 한 개의 범주형 변수

# [실습 예제]
# 주사위를 60회 던졌을 때, 각 눈의 빈도가 이론적 확률(각 10회)에 적합한지 검정

# 귀무가설(H0): 관찰 빈도는 기대 빈도와 차이가 없다. (주사위가 공정하다)
# 대립가설(H1): 관찰 빈도는 기대 빈도와 차이가 있다. (주사위가 공정하지 않다)

import pandas as pd
import scipy.stats as stats

# 관측 데이터 (주사위 눈 1~6이 나온 횟수)
observed_data = [4, 6, 17, 16, 8, 9]

chi2, p_value = stats.chisquare(observed_data)
print(f"카이제곱 통계량: {chi2}")
print(f"p-value: {p_value}")  # p-value: 0.014387678176921308

# [판정] 유의수준 0.05 기준
# p-value(0.014) < 0.05 이므로 귀무가설을 기각하고 대립가설을 채택한다.
# 검정에 사용된 데이터는 우연히 발생한 것이 아니라 필연적인 원인에 발생한 것이다.
# 결론: 이 주사위는 각 눈이 나올 확률이 균등하지 않다고 볼 수 있다.

# df = n - 1 = 6 - 1 = 5
# 유의 수준 : 0.05